import mongoose from "mongoose";

export async function testConnection() {
  try {
    await connect();
    await disconnect();
    console.log("‚úÖ Database connection test was successful (connect + disconnect)");
  } catch (error) {
    console.log("‚ùå Error testing database connection. Error:", error);
  }
}

export async function connect() {
  try {
    if (!process.env.DBHOST) {
      throw new Error("‚ùå DBHOST environment variable is not defined");
    }

    // üîç Logg MongoDB URL 
    console.log("üåê Connecting to MongoDB:", process.env.DBHOST);

    await mongoose.connect(process.env.DBHOST);

    if (mongoose.connection.db) {
      await mongoose.connection.db.admin().command({ ping: 1 });
      console.log("‚úÖ Connection to MongoDB established");
    } else {
      throw new Error("‚ùå Database connection is not established");
    }
  } catch (error) {
    console.log("‚ùå Error connecting to the database. Error:", error);
  }
}

export async function disconnect() {
  try {
    await mongoose.disconnect();
    console.log("üîå Connection to MongoDB closed");
  } catch (error) {
    console.log("‚ùå Error closing database connection. Error:", error);
  }
}
